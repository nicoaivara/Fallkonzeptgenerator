// Punkte berechnen
window.scoreMechanism = function(mech, selectedSymptomIds) {
  const matched = [];
  let score = 0;
  for (const [sid, pts] of mech.rules) {
    if (selectedSymptomIds.has(sid)) {
      score += pts;
      matched.push({ id: sid, pts });
    }
  }
  return { score, matched };
};
